    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newMeal = new Meal;
        $newMeal->name = $request->name;
    
        $newMeal->save();

        return redirect()->action('MealController@show', [$newMeal]);
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $meal = Meal::find($id);
        return view('ingredient', compact('meal'));
        //return redirect()->action('MealController@show', [$meal]);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $meal = Meal::find($id);
        return view('ingredient', compact('meal'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $meal = Meal::find($id);
        //need to append meals, not replace them. 
        $meal->ingredients = json_encode([$request->ingredientName, $request->protein, $request->carbohydrates, $request->fat]);

        $meal->save();

        return view('ingredient', compact('meal'));
    }

